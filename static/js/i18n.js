// ── Motor ──────────────────────────────────────────────────────────────────

let currentLang = localStorage.getItem('i18n_lang') || 'pt';

function t(key) {
  const d = I18N[currentLang];
  return (d && d[key] !== undefined) ? d[key] : (I18N['pt'][key] || key);
}
window.t = t;

function applyI18n(lang) {
  currentLang = lang;
  localStorage.setItem('i18n_lang', lang);
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';

  // textContent
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.getAttribute('data-i18n'));
    el.textContent = val;
  });

  // placeholder
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });

  // title attribute
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.getAttribute('data-i18n-title'));
  });

  // Badges de tipo (Direto / Indireto / A Classificar)
  document.querySelectorAll('[data-badge]').forEach(el => {
    const type = el.getAttribute('data-badge');
    if (type === 'direto')      el.textContent = t('vis.badge_direto');
    else if (type === 'indireto') el.textContent = t('vis.badge_indireto');
    else if (type === 'classificar') el.textContent = t('vis.a_classificar');
  });

  // Valores de pluviometria (spans com data-pluv-pt)
  document.querySelectorAll('[data-pluv-pt]').forEach(el => {
    const ptVal = el.getAttribute('data-pluv-pt');
    el.textContent = t('pluv.' + ptVal);
  });

  // Botão toggle
  const btn = document.getElementById('langToggleBtn');
  if (btn) {
    const ptActive = lang === 'pt';
    btn.innerHTML =
      `<span style="opacity:${ptActive?'1':'.38'};font-weight:${ptActive?'700':'400'}">🇧🇷&nbsp;PT</span>` +
      `<span style="opacity:.3;margin:0 .25rem">|</span>` +
      `<span style="opacity:${ptActive?'.38':'1'};font-weight:${ptActive?'400':'700'}">🇺🇸&nbsp;EN</span>`;
  }

  // Notifica gráficos e outros módulos
  document.dispatchEvent(new CustomEvent('i18nChanged', { detail: { lang } }));
}
window.applyI18n = applyI18n;

function toggleLang() {
  applyI18n(currentLang === 'pt' ? 'en' : 'pt');
}
window.toggleLang = toggleLang;

// Aplica ao carregar o DOM
(function () {
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => applyI18n(currentLang));
  } else {
    applyI18n(currentLang);
  }
})();
